package it.univpm.JavaEsame.Model;

public class Operation {

	private double avg;
	private double max;
	private double min;
	private double dev_std;
	private double sum;
	private int count;
	
	public Operation(double avg, double max, double min, double dev_std, double sum, int count)
	{
		this.avg = avg;
		this.max = max;
		this.min = min;
		this.dev_std = dev_std;
		this.sum = sum;
		this.count = count;
	}

	

	
}
